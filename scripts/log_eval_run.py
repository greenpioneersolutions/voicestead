#!/usr/bin/env python3
"""Log one eval run into the public run ledger (docs/evals/).

Reads an eval --out directory (benchmark.json is required; scorecard.md's headline is
quoted when present), writes docs/evals/YYYY-MM-DD-<slug>.md, then regenerates the
index table in docs/evals/README.md from the entry files on disk.

EVERY figure in an entry is copied from an artifact: benchmark.json fields written by
tests/run_eval.py, and the commit from `git rev-parse --short HEAD`. This script never
computes, estimates, or defaults a number — a required field missing from
benchmark.json is a refusal (exit 1), never a blank cell or a guess.

  python3 scripts/log_eval_run.py tests/results --slug weekly-sweep --notes "what changed"
"""
import argparse
import json
import os
import re
import subprocess
import sys

REPO = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DEFAULT_DIR = os.path.join(REPO, "docs", "evals")
BEGIN = "<!-- BEGIN RUN INDEX (generated by scripts/log_eval_run.py; edits between markers are overwritten) -->"
END = "<!-- END RUN INDEX -->"
_FIELD = re.compile(r"^- \*\*(.+?):\*\* (.*)$", re.M)
# every field the index table copies out of an entry file
_INDEX_FIELDS = ("Date", "Backend", "Cases", "Blind win rate", "Git commit")


def _refuse(msg):
    print("REFUSED: %s" % msg, file=sys.stderr)
    sys.exit(1)


def _require(obj, dotted):
    """Fetch metadata.foo.bar or refuse. Key presence is required; a JSON null is a
    legitimate artifact value (e.g. an uncomputable win rate) and passes through."""
    cur = obj
    for key in dotted.split("."):
        if not isinstance(cur, dict) or key not in cur:
            _refuse("benchmark.json is missing required field %r — the ledger copies "
                    "numbers from artifacts, it never invents them" % dotted)
        cur = cur[key]
    return cur


def _git_commit():
    try:
        out = subprocess.run(["git", "rev-parse", "--short", "HEAD"], cwd=REPO,
                             capture_output=True, text=True, check=True).stdout.strip()
    except Exception as e:  # noqa: BLE001 — any git failure means no commit to copy
        _refuse("could not read the git commit (%s) — the ledger records the exact "
                "commit or nothing" % e)
    if not out:
        _refuse("git rev-parse returned nothing — the ledger records the exact commit or nothing")
    return out


def _win_string(bench):
    rate = _require(bench, "overall.win_rate.rate")
    if rate is None:
        return "n/a (no valid pairwise judgments)"
    wins = _require(bench, "overall.win_rate.wins")
    ties = _require(bench, "overall.win_rate.ties")
    losses = _require(bench, "overall.win_rate.losses")
    n_valid = _require(bench, "overall.win_rate.n_valid")
    return "%s (%sW/%sT/%sL of %s valid)" % (rate, wins, ties, losses, n_valid)


def _usage_strings(bench):
    """Token/cost lines — only what the backend itself reported, otherwise say so."""
    usage = bench["metadata"].get("llm_usage") or {}
    tokens = "not reported by this backend"
    cost = "not reported by this backend"
    if usage.get("calls") and (usage.get("input_tokens") or usage.get("output_tokens")):
        tokens = "%s in / %s out (%s calls)" % (usage["input_tokens"],
                                                usage["output_tokens"], usage["calls"])
    if usage.get("cost_reported"):
        cost = "%s" % usage["total_cost_usd"]
    return tokens, cost


def _scorecard_headline(out_dir):
    path = os.path.join(out_dir, "scorecard.md")
    if not os.path.isfile(path):
        return None
    for line in open(path):
        if line.startswith("**Overall blind win rate"):
            return line.strip()
    return None


def build_entry(bench, out_dir, slug, notes):
    ts = _require(bench, "metadata.timestamp")
    date = ts[:10]
    if not re.fullmatch(r"\d{4}-\d{2}-\d{2}", date):
        _refuse("metadata.timestamp %r does not start with a YYYY-MM-DD date" % ts)
    tokens, cost = _usage_strings(bench)
    lines = [
        "# Eval run — %s — %s" % (date, slug),
        "",
        "- **Date:** %s" % date,
        "- **Backend:** %s" % _require(bench, "metadata.backend"),
        "- **Generator model:** %s" % _require(bench, "metadata.skill_model"),
        "- **Judge model:** %s" % _require(bench, "metadata.judge_model"),
        "- **Cases:** %s" % _require(bench, "overall.cases"),
        "- **Runs per configuration:** %s" % _require(bench, "metadata.runs_per_configuration"),
        "- **Blind win rate:** %s" % _win_string(bench),
        "- **Cases with hard-gate failures:** %s" % _require(bench, "overall.cases_with_hard_failures"),
        "- **Judge samples failed:** %s" % _require(bench, "overall.judge_failed_samples"),
        "- **Tokens:** %s" % tokens,
        "- **Cost (USD):** %s" % cost,
        "- **Git commit:** %s" % _git_commit(),
    ]
    command = bench["metadata"].get("command")
    if command:
        lines.append("- **Command:** `%s`" % command)
    headline = _scorecard_headline(out_dir)
    if headline:
        lines += ["", "> %s" % headline]
    if notes:
        lines += ["", "## Notes", "", notes]
    lines.append("")
    return date, "\n".join(lines)


def _index_row(evals_dir, name):
    fields = dict(_FIELD.findall(open(os.path.join(evals_dir, name)).read()))
    for want in _INDEX_FIELDS:
        if want not in fields:
            _refuse("%s lacks the %r field the index copies from — fix or remove the entry"
                    % (name, want))
    label = name[:-len(".md")]
    return "| %s | %s | %s | %s | %s | [%s](%s) |" % (
        fields["Date"], fields["Backend"], fields["Cases"], fields["Blind win rate"],
        fields["Git commit"], label, name)


def regenerate_index(evals_dir):
    readme = os.path.join(evals_dir, "README.md")
    if not os.path.isfile(readme):
        _refuse("%s not found — the ledger needs its README (with index markers) in place"
                % os.path.relpath(readme, REPO))
    text = open(readme).read()
    if BEGIN not in text or END not in text:
        _refuse("README.md lacks the index markers this script regenerates between")
    rows = [_index_row(evals_dir, name)
            for name in sorted(os.listdir(evals_dir), reverse=True)
            if name.endswith(".md") and name != "README.md"]
    if rows:
        block = "\n".join(["| Date | Backend | Cases | Blind win rate | Commit | Entry |",
                           "|---|---|---|---|---|---|"] + rows)
    else:
        block = "No runs yet — the first entry lands with S0."
    new = (text[:text.index(BEGIN) + len(BEGIN)] + "\n" + block + "\n"
           + text[text.index(END):])
    with open(readme, "w") as f:
        f.write(new)
    return len(rows)


def main():
    ap = argparse.ArgumentParser(description="Copy one eval run's artifacts into docs/evals/.")
    ap.add_argument("out_dir", help="the eval --out directory (must contain benchmark.json)")
    ap.add_argument("--slug", required=True, help="short entry name, e.g. weekly-sweep")
    ap.add_argument("--notes", default="", help="free-text context for the entry")
    ap.add_argument("--dir", default=DEFAULT_DIR,
                    help="ledger directory (default: docs/evals; tests point elsewhere)")
    args = ap.parse_args()
    if not re.fullmatch(r"[a-z0-9][a-z0-9-]*", args.slug):
        ap.error("--slug must be lowercase letters/digits/hyphens (it becomes a filename)")

    bench_path = os.path.join(args.out_dir, "benchmark.json")
    if not os.path.isfile(bench_path):
        _refuse("%s not found — point at an eval --out directory" % bench_path)
    bench = json.load(open(bench_path))

    date, entry = build_entry(bench, args.out_dir, args.slug, args.notes)
    entry_path = os.path.join(args.dir, "%s-%s.md" % (date, args.slug))
    if os.path.exists(entry_path):
        _refuse("%s already exists — pick another --slug rather than overwriting a record"
                % entry_path)
    # validate the README before writing anything, so a refusal leaves no half-state
    regenerate_index(args.dir)
    with open(entry_path, "w") as f:
        f.write(entry)
    n = regenerate_index(args.dir)
    print("wrote %s and regenerated the index (%d entries)" % (entry_path, n))


if __name__ == "__main__":
    main()
